package com.example.comradeappsoftware.models;

public class User {


    private int distritos_iddistrito;
    private String dni;
    private String fec_registro;
    private String password;
    private String email;
    private String nombres;
    private String apellidos;
    private int idusuario;

    public int getDistritos_iddistrito() {
        return distritos_iddistrito;
    }

    public void setDistritos_iddistrito(int distritos_iddistrito) {
        this.distritos_iddistrito = distritos_iddistrito;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getFec_registro() {
        return fec_registro;
    }

    public void setFec_registro(String fec_registro) {
        this.fec_registro = fec_registro;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public int getIdusuario() {
        return idusuario;
    }

    public void setIdusuario(int idusuario) {
        this.idusuario = idusuario;
    }
}







